DROP TABLE IF EXISTS CUSTOMERS;

CREATE TABLE CUSTOMERS
(
  CUSTOMER_SYS_ID      BIGINT  NOT NULL,
  CUSTOMER_CODE         VARCHAR(100) NOT NULL,
  COMPANY_NAME         VARCHAR(255) NOT NULL,
  COMPANY_BUSINESS     VARCHAR(255),
  LANDING_PROD_SYS_ID  BIGINT  NOT NULL,
  ACTIVE_STATUS_IND    TINYINT NOT NULL,
  CREATED_DATE     DATETIME NOT NULL,
  CREATED_BY       VARCHAR(255) NOT NULL,
  INACTIVATED_DATE   DATETIME,
  INACTIVATED_BY     VARCHAR(255),
  MODIFIED_DATE     DATETIME,
  MODIFIED_BY        VARCHAR(255),
  PASSWORD_EXPIRY_DAYS INT NOT NULL,
  DOMAIN_NAME		   VARCHAR(100) NOT NULL,
  IS_JV_CUSTOMER        TINYINT(4) NOT NULL
)ENGINE=InnoDB;

  
  
  
