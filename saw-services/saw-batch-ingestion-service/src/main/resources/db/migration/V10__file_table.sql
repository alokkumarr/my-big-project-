DROP TABLE IF EXISTS `SIP_BIS_TRANSFER`;
CREATE TABLE `SIP_BIS_TRANSFER`
(
  TRANSFER_ID BIGINT  NOT NULL AUTO_INCREMENT,
  JOB_ID BIGINT NOT NULL,
  FILE_NAME VARCHAR(50),
  FILE_PATH  VARCHAR(255),
  START_TIME DATETIME,
  END_TIME DATETIME,
  DURATION BIGINT ,
  STATUS VARCHAR(50),
  FILE_PATTERN VARCHAR(255),
  TARGET_FILE_PATH VARCHAR(255),
  CREATED_TIME DATETIME,
  UPDATED_TIME DATETIME,
  STATE_REASON TEXT,
  CONSTRAINT SIP_BIS_TRANSFER_PK PRIMARY KEY (TRANSFER_ID),
  CONSTRAINT SIP_BIS_TRANSFER_FK FOREIGN KEY (JOB_ID) 
  REFERENCES SAW_SCHEDULER.SIP_JOB(JOB_ID)
)ENGINE=InnoDB;